import type { SessionState, TargetContext, ScenarioGuidance } from "@/lib/types";

export function buildReportGenerationPrompt(params: {
  targetContext: TargetContext;
  scenarioGuidance?: ScenarioGuidance;
  scoredDimensions: any[];
  sessionState?: SessionState;
}): string {
  const { targetContext, scenarioGuidance, scoredDimensions } = params;

  return `## 场景选择指导

你需要为"风格对比"和"Prompt模板"选择一个具体的使用场景。

### 用户信息

- 职业/身份：${targetContext.role}
- 最近用AI做什么：${targetContext.recentUse}
- 中途反馈场景：${scenarioGuidance?.scenarioSummary || '无'}
- 常用工具：${targetContext.tools?.join('、') || '无'}

### 场景来源优先级（从高到低）

**1. 优先使用 recentUse**（如果具体）
- 判断标准：如果描述了明确的任务或活动（如"写产品需求文档"、"分析销售数据"），就是具体的
- 如果只是"使用 AI 完成日常任务"、"日常使用"等泛泛描述，就不具体

**2. 使用 scenarioSummary**（如果有且具体）
- 判断标准：如果描述了明确的情境（如"在团队协作时"、"写论文时"），就是具体的

**3. 根据 role 推断场景**（如果上述都不具体）
- 推断方法：根据职业推断最常见的AI使用场景

常见职业的典型场景参考：
- 程序员/开发者 → 写代码和调试
- 产品经理 → 写产品需求文档
- 设计师 → 设计界面或创作
- 数据分析师 → 分析数据
- 编辑/运营 → 编辑内容或写文案
- 市场/营销 → 写营销方案
- 学生 → 学习和完成作业
- 研究生/博士 → 做研究和写论文
- 教师/讲师 → 准备教学内容
- 咨询/顾问 → 分析商业问题
- 销售/商务 → 准备销售材料

**4. 使用通用场景**（兜底）
- 如果以上都无法推断出具体场景，使用："完成一个复杂任务"

### 场景选择要求

- 场景必须具体、可执行，不要太抽象
- 场景要符合用户的实际情况（role + recentUse + scenarioSummary）
- 如果是通用场景，对比内容仍要具体可执行，不要写"根据任务类型..."这类占位表达
- 在 selectedScenario 字段输出你选择的场景

### 示例

示例1（recentUse具体）：
- role: "产品经理"
- recentUse: "写产品需求文档"
- 选择场景: "写产品需求文档"

示例2（recentUse泛，根据role推断）：
- role: "前端开发工程师"
- recentUse: "使用 AI 完成日常任务"
- 选择场景: "开发前端功能"

示例3（都不具体）：
- role: "用户"
- recentUse: "使用 AI 完成日常任务"
- 选择场景: "完成一个复杂任务"

---

## AI-MBTI 维度定义

服务端已计算出的维度结果（必须原样沿用，不要重算）：
${JSON.stringify(scoredDimensions, null, 2)}

### 各维度方向

**Relation（关系定位）：**
- 低分（0-40）= 工具型：把AI当工具，直接给指令
- 高分（60-100）= 伙伴型：把AI当伙伴，会主动邀请讨论

**Workflow（工作流程）：**
- 低分（0-40）= 框架型：先定框架/规则再让AI执行
- 高分（60-100）= 探索型：先让AI探索再逐步调整

**Epistemic（认知态度）：**
- 低分（0-40）= 审计型：会验证AI输出、查文档核对
- 高分（60-100）= 信任型：直接采纳AI输出、较少质疑

**RepairScope（修复范围）：**
- 低分（0-40）= 全局型：全局重开、重新描述需求
- 高分（60-100）= 局部型：局部修改、小步迭代

---

## 内容生成要求

### 1. 风格画像（styleProfile）

#### 1.1 你是这样用AI的（behaviors）

生成3-4条具体行为描述，每条包括：
- behavior：具体行为（如"你习惯先把目标和边界说清楚，再让AI开始工作"）
- basedOn：基于哪个维度（如"框架型 75分"）
- evidence：证据（引用用户原话或题目回答）

要求：
- 每条都要具体，不要抽象
- 必须有证据支撑
- 覆盖用户最突出的2-3个维度

#### 1.2 对比：不同风格的人会怎么做（comparison）

基于你选择的场景（selectedScenario），生成对比内容：

- scenario：你选择的场景
- styles：至少2种风格的对比（包括用户的风格）

每种风格包括：
- type：风格类型（如"你的风格（框架型+审计型）"）
- approach：具体做法（50-120字）
- pros：优势（20-60字）
- cons：风险（20-60字）

要求：
- 做法要具体可执行，不要抽象
- 优势和风险要真实，不要只说好话
- 如果是通用场景，仍要写得具体

#### 1.3 你的独特组合（uniqueness）

- combination：用户的倾向组合（如"框架型 + 审计型"）
- percentage：稀有度（可以估算，如"约15%的用户和你类似"）
- similarRoles：相似用户群体（根据倾向组合推断，如["产品经理", "技术架构师"]）

### 2. 问题诊断（problems）

生成3个用户可能遇到的问题，每个问题包括：

- title：问题标题（简短有力，如"AI总是给不出你想要的结果"）
- symptom：症状描述（用户能感知到的现象，30-80字）
- why：原因解释（基于用户的倾向组合，40-100字）
- howToFix：解决方案
  - immediate：立刻可以尝试的动作（30-80字）
  - example：具体例子（用 ❌ 错误做法 vs ✅ 正确做法 的格式）
  - expectedResult：预期效果（20-50字）
- basedOn：基于哪些倾向（如"框架型 75分 + 审计型 80分"）

#### 问题选择策略

根据用户的倾向组合，选择最相关的3个问题：

1. 优先选择用户最极端的维度相关的问题（分数<30或>70）
2. 选择有明显组合特征的问题（如框架型<30 + 审计型<30）
3. 确保3个问题覆盖不同的维度
4. 如果用户的分数都在40-60之间，选择最接近边界的维度

常见问题参考：

**框架型（低分）相关：**
- "框架定得太细，AI没有发挥空间" - 适用于：框架型<30 + 审计型>60
- "AI总是给不出你想要的结果" - 适用于：框架型<40
- "花太多时间定框架，效率不高" - 适用于：框架型<30 + 局部型<40

**探索型（高分）相关：**
- "探索太多方向，难以收敛" - 适用于：探索型>70 + 信任型>60
- "AI给的方案经常不靠谱" - 适用于：探索型>70 + 信任型>70
- "想法太多，不知道选哪个" - 适用于：探索型>70

**审计型（低分）相关：**
- "验证AI输出花的时间比自己写还多" - 适用于：审计型<30 + 框架型<40
- "总是挑出AI的问题，改来改去" - 适用于：审计型<30 + 局部型>60
- "对AI的输出不放心，反复检查" - 适用于：审计型<40

**信任型（高分）相关：**
- "AI的方案经常不靠谱，但我直接用了" - 适用于：信任型>70 + 探索型>60
- "容易被AI带偏，偏离原本目标" - 适用于：信任型>70
- "发现问题时已经走了很远" - 适用于：信任型>70 + 局部型>60

**伙伴型（高分）相关：**
- "和AI对话越来越乱，上下文混乱" - 适用于：伙伴型>60 + 局部型>60
- "讨论太多，迟迟不进入执行" - 适用于：伙伴型>70 + 探索型>60
- "AI的建议太多，不知道听哪个" - 适用于：伙伴型>70 + 信任型>60

**工具型（低分）相关：**
- "AI只是执行指令，没有给我新想法" - 适用于：工具型<30 + 框架型<40
- "感觉AI的价值没有发挥出来" - 适用于：工具型<40
- "AI的输出很机械，缺少灵活性" - 适用于：工具型<30 + 框架型<30

**局部型（高分）相关：**
- "改了很多轮，整体结构还是有问题" - 适用于：局部型>70
- "局部看起来都对，但整体不协调" - 适用于：局部型>70 + 伙伴型>60
- "每次只改一点，效率很低" - 适用于：局部型>70 + 审计型<40

**全局型（低分）相关：**
- "经常推倒重来，浪费之前的工作" - 适用于：全局型<30 + 探索型>60
- "不愿意在现有基础上调整" - 适用于：全局型<30
- "重开次数太多，时间成本高" - 适用于：全局型<30 + 框架型<40

### 3. 工具箱（toolbox）

#### 3.1 Prompt模板（promptTemplates）

生成3-5个Prompt模板，每个包括：
- title：模板标题（≤12字，有场景感，如"结构化探索"）
- useCase：使用时机（≤40字）
- prompt：Prompt内容（80-250字，第一人称，可直接复制使用）
- tags：标签（如["框架型", "适合探索阶段"]）

要求：
- prompt必须绑定用户的真实场景（selectedScenario）
- 如果场景具体，直接写进prompt（如"我在写产品需求文档..."）
- 如果场景是通用的，用简短的占位符（如"我在[具体任务]..."）
- prompt要体现用户的倾向特点（如框架型的模板会先定边界）
- 不要用"必须"、"严禁"等生硬的词，用"咱们先..."、"你先...我再..."

#### 3.2 Checklist（checklists）

生成2个Checklist：
1. "使用AI前的Checklist"（4-5项）
2. "收到AI输出后的Checklist"（4-5项）

要求：
- 每项都要具体可执行
- 根据用户的倾向调整（如审计型的checklist会更强调验证）

#### 3.3 工作流（workflow）

生成一个适合用户的工作流，包括：
- title：工作流标题（如"适合你的AI协作流程"）
- steps：5-7个步骤，每个步骤包括：
  - step：步骤编号
  - action：动作（简短，如"明确目标和核心约束"）
  - detail：详细说明（20-60字）
  - time：预计时间（如"2分钟"）
- totalTime：总时间（如"约25分钟"）
- basedOn：基于哪些倾向（如"框架型 + 审计型"）

要求：
- 工作流要符合用户的倾向（如框架型会先定框架，探索型会先发散）
- 步骤要具体可执行
- 时间估算要合理

---

请根据以上指导生成完整的报告内容。`;
}
